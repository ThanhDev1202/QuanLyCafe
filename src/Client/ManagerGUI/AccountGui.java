    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = JOptionPane.showInputDialog(this, "Nhập username:");
        if (username == null) {
            return;
        }

        String password = JOptionPane.showInputDialog(this, "Nhập password:");
        if (password == null) {
            return;
        }

        String displayname = JOptionPane.showInputDialog(this, "Nhập tên hiển thị:");
        if (displayname == null) {
            return;
        }

        String[] options = {"0 - Staff", "1 - Manager"};
        String choice = (String) JOptionPane.showInputDialog(this, "Chọn loại tài khoản:", "Type",
                JOptionPane.QUESTION_MESSAGE, null, options, options[0]);

        if (choice == null) {
            return;
        }
        int type = choice.startsWith("0") ? 0 : 1;

        Account ac = new Account(displayname, username, password, type);

        try {
            Request req = new Request("ADD ACCOUNT", ac);
            ClientConnection.getOut().writeObject(req);
            ClientConnection.getOut().flush();
            Response res = (Response) ClientConnection.getIn().readObject();
            JOptionPane.showMessageDialog(this, res.getMessage());
            if (res.getStatus().equals("SUCCESS")) {
                load();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
