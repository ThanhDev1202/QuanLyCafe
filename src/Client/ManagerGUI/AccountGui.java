    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn tài khoản cần xóa!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa tài khoản ID: " + jTable1.getValueAt(row, 0) + "?",
                "Xác nhận", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            int id = (int) jTable1.getValueAt(row, 0);
            Account ac = new Account();
            ac.setId(id);

            try {
                Request req = new Request("DELETE ACCOUNT", ac);
                ClientConnection.getOut().writeObject(req);
                ClientConnection.getOut().flush();
                Response res = (Response) ClientConnection.getIn().readObject();
                JOptionPane.showMessageDialog(this, res.getMessage());
                if (res.getStatus().equals("SUCCESS")) {
                    load();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    public void load() {
        try {
            Request req = new Request("GET ACCOUNTS", null);
            ClientConnection.getOut().writeObject(req);
            ClientConnection.getOut().flush();
            Response res = (Response) ClientConnection.getIn().readObject();

            if (res.getStatus().equals("SUCCESS")) {
                List<Account> list = (List<Account>) res.getData();
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);
                for (Account ac : list) {
                    String role = ac.getType() == 0 ? "Staff" : "Manager";
                    model.addRow(new Object[]{ac.getId(), ac.getUsername(), ac.getDisplayName(), ac.getType()});
                }
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi: " + res.getMessage());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

