    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn tài khoản trong bảng!");
            return;
        }

        int id = (int) jTable1.getValueAt(row, 0);
        String[] options = {"0 - Staff", "1 - Manager"};
        String choice = (String) JOptionPane.showInputDialog(this, "Chọn loại tài khoản mới:", "Sửa Type",
                JOptionPane.QUESTION_MESSAGE, null, options, options[0]);

        if (choice != null) {
            Account ac = new Account();
            ac.setId(id);
            ac.setType(choice.startsWith("0") ? 0 : 1);

            try {
                Request req = new Request("UPDATE ACCOUNT", ac);
                ClientConnection.getOut().writeObject(req);
                ClientConnection.getOut().flush();
                Response res = (Response) ClientConnection.getIn().readObject();
                JOptionPane.showMessageDialog(this, res.getMessage());
                if (res.getStatus().equals("SUCCESS")) {
                    load();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
