    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         try {
        // Lấy ID từ textfield
        int id = Integer.parseInt(jTextField3.getText().trim());

        // Tạo object gửi server
        TableFood tbf = new TableFood();
        tbf.setId(id);

        // Gửi request xóa
        Request req = new Request("DELETE_TABLE", tbf);
        Login.out.writeObject(req);
        Login.out.flush();

        // Nhận phản hồi
        Response res = (Response) Login.in.readObject();
        JOptionPane.showMessageDialog(this, res.getMessage());

        // Nếu xóa thành công thì load lại bảng
        if (res.getStatus().equals("SUCCESS")) {
            load();
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID phải là số!");
    } catch (Exception e) {
        e.printStackTrace();
    }
        
   
    }//GEN-LAST:event_jButton3ActionPerformed

